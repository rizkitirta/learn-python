# level operator
# 1. tanda kurung ()
tanda_kurung = 5 * (5 + 5) / 5 - 5
print(tanda_kurung)

# 2. (perpangkatan) **
perpangkatan = 5 + 10 * 10 /  5 ** 2
print(perpangkatan)

# 3. (perkalian dan pembagian) * /
perkalian_dan_pembagian = 5 + 10 * 10 / 5
print(perkalian_dan_pembagian)

# 4. (penjumlahan dan pengurangan) + -
penjumlahan_dan_pengurangan = 100 - 5 + 5
print(penjumlahan_dan_pengurangan)


